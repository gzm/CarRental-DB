CREATE TABLE CHECK_IN
    (  CHECK_IN_ID NUMBER(20) NOT NULL PRIMARY KEY CHECK(0<CHECK_IN_ID),
       EKSTRA_NAME VARCHAR2(50) NOT NULL,
       CI_EXTRA_PAY NUMBER(10) NOT NULL
    );

CREATE TABLE CHECK_OUT
    (  CHECK_OUT_ID NUMBER(20) NOT NULL PRIMARY KEY CHECK(0<CHECK_OUT_ID),
       ACCIDENT VARCHAR2(20),
       PUNISHMENT VARCHAR2(20),
       KM_DIFFERENCE NUMBER(10) NOT NULL,
       CO_EXTRA_PAY NUMBER(10) NOT NULL
    );

CREATE TABLE GTYPE
    (  GTYPE_ID NUMBER(20) NOT NULL PRIMARY KEY CHECK(0<GTYPE_ID),
       GTYPE VARCHAR2(100)
     );

CREATE TABLE CMAINTENANCE
    (   MAINTENANCE_ID NUMBER(20) NOT NULL PRIMARY KEY CHECK(0<MAINTENANCE_ID),
        TRAFFIC_INSURANCE_DATE DATE,
        AUTOMOBILE_INSURANCE_DATE DATE,
        PERIODIC_DATE DATE
     );

CREATE TABLE CTYPE
    (  CAR_TYPE_ID NUMBER(20) NOT NULL PRIMARY KEY CHECK(0<CAR_TYPE_ID),
       CLASS VARCHAR2(10) 
     );

CREATE TABLE BRANCHES
    (  BRANCH_ID NUMBER(20) NOT NULL PRIMARY KEY CHECK(0<BRANCH_ID),
       BRANCH_NAME VARCHAR2(60),
       STATE VARCHAR2(50),
       CITY VARCHAR2(50),
       BPHONE NUMBER(20),
       BNUMBER_OF_CARS NUMBER(2)
     );

CREATE TABLE CUSTOMER
    (  CUST_ID NUMBER(20) NOT NULL PRIMARY KEY CHECK(0<CUST_ID),
       CNAME VARCHAR2(50) NOT NULL,
       CSURNAME VARCHAR2(50) NOT NULL,
       CPHONE NUMBER(20) NOT NULL,
       CBIRTDATE DATE NOT NULL,
       LICENCE_DATE DATE NOT NULL,
       DCLASS VARCHAR2(20) NOT NULL,
       BRANCH_ID NUMBER(20) NOT NULL,
       FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES(BRANCH_ID)
    );

CREATE TABLE SERVICE
    (
        BRANCH_ID NUMBER(20),
        CUST_ID NUMBER(20),
        FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES(BRANCH_ID),
        FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID),
        PRIMARY KEY(BRANCH_ID, CUST_ID)
    );

CREATE TABLE EMPLOYEES
    (  EMPLOYEE_ID NUMBER(20) NOT NULL PRIMARY KEY CHECK(0<EMPLOYEE_ID),
       ENAME VARCHAR2(20) NOT NULL,
       ESURNAME VARCHAR2(20) NOT NULL,
       EADDRESS VARCHAR2(20),
       TELEPHONE NUMBER(20) NOT NULL,
       START_DATE DATE,
       SALARY NUMBER(10) NOT NULL,
       BRANCH_ID NUMBER(20) NOT NULL,
       FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES (BRANCH_ID)  
     );

CREATE TABLE CAR
    (CAR_ID NUMBER(20) NOT NULL PRIMARY KEY CHECK(0<CAR_ID), 
     COLOR VARCHAR2(100), 
     LICENCE_PLATE VARCHAR2 (20)NOT NULL,
     MILLEAGE NUMBER NOT NULL,
     CAR_PAYMENT NUMBER NOT NULL,    
     CAR_FUEL VARCHAR2(50) NOT NULL,
     CAR_BRAND VARCHAR2(50) NOT NULL,
     CAR_MODEL VARCHAR2(50) NOT NULL,
     CAR_YEAR NUMBER(4) NOT NULL,
     SEATING_CAPACITY NUMBER NOT NULL,
     GTYPE_ID NUMBER NOT NULL,
     MAINTENANCE_ID NUMBER NOT NULL,
     CAR_TYPE_ID NUMBER NOT NULL,
     BRANCH_ID NUMBER NOT NULL,
        FOREIGN KEY (GTYPE_ID) REFERENCES GTYPE(GTYPE_ID),
        FOREIGN KEY (MAINTENANCE_ID) REFERENCES CMAINTENANCE(MAINTENANCE_ID),
        FOREIGN KEY (CAR_TYPE_ID) REFERENCES CTYPE(CAR_TYPE_ID),
        FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES(BRANCH_ID)
    );

CREATE TABLE PAYMENT
    (  PAYMENT_ID NUMBER(20) NOT NULL PRIMARY KEY CHECK(0<PAYMENT_ID),
       CHECK_IN_ID NUMBER(20) NOT NULL,
       CHECK_OUT_ID NUMBER(20) NOT NULL,
       CAR_ID NUMBER(20) NOT NULL,
       FOREIGN KEY (CHECK_IN_ID) REFERENCES CHECK_IN (CHECK_IN_ID),
       FOREIGN KEY (CHECK_OUT_ID) REFERENCES CHECK_OUT (CHECK_OUT_ID),
       FOREIGN KEY (CAR_ID) REFERENCES CAR (CAR_ID) 
    );

CREATE TABLE CONTRACT
    (  CONTRACT_ID NUMBER(20) NOT NULL PRIMARY KEY CHECK(0<CONTRACT_ID),
       RENT_DATE DATE NOT NULL,
       RETURN_DATE DATE NOT NULL, 
       CUST_ID NUMBER(20) NOT NULL,
       EMPLOYEE_ID NUMBER(20) NOT NULL,
       PAYMENT_ID NUMBER(20) NOT NULL,
       CAR_ID NUMBER(20) NOT NULL,
       FOREIGN KEY (PAYMENT_ID) REFERENCES PAYMENT (PAYMENT_ID),
       FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER (CUST_ID),
       FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES (EMPLOYEE_ID),
       FOREIGN KEY (CAR_ID) REFERENCES CAR (CAR_ID)  
    );


